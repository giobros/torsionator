_OUTPUT_NAMES = ["scanning", "conformers", "parameters", "workflow.log"]


def backup_existing_outputs(base_dir: str) -> Optional[int]:
    """Rename pre-existing torsionator output dirs/files to bck.X.<name>.

    Follows the PLUMED convention: bck.0.<name>, bck.1.<name>, …
    All items from the same previous run get the same index X, chosen as
    the lowest integer for which no bck.X.* entry already exists in base_dir.
    Returns the backup index used, or None if nothing needed backing up.
    """
    # Collect items that exist
    items: List[str] = []
    for name in _OUTPUT_NAMES:
        if os.path.exists(os.path.join(base_dir, name)):
            items.append(name)
    for path in _glob.glob(os.path.join(base_dir, "minimized_*.pdb")):
        items.append(os.path.basename(path))

    if not items:
        return None

    # Find the lowest unused backup index
    used: set = set()
    if os.path.isdir(base_dir):
        for entry in os.listdir(base_dir):
            parts = entry.split(".")
            if len(parts) >= 3 and parts[0] == "bck":
                try:
                    used.add(int(parts[1]))
                except ValueError:
                    pass

    idx = 0
    while idx in used:
        idx += 1

    for name in items:
        src = os.path.join(base_dir, name)
        dst = os.path.join(base_dir, f"bck.{idx}.{name}")
        os.rename(src, dst)
        print(f"Backup: {name} -> bck.{idx}.{name}")

    return idx


