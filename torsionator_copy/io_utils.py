def detect_basis_from_antechamber(
    log,
    cfg,
    d_tup: Tuple[int, int, int, int],
    method: str,
) -> Tuple[int, ...]:
    """Read the periodicity terms from the GAFF2/old antechamber frcmod for
    *d_tup* and return them as a basis tuple (same sign convention as
    ``_basis_from_max_mult``).  Call this after ``run_gaff2_old`` has finished.
    """
    ante_dir = _gaff2_old_ante_dir(cfg, d_tup, method)
    frcmod_path = os.path.join(ante_dir, "MOL.frcmod")
    mol2_path = os.path.join(ante_dir, "MOL.mol2")
    basis = read_basis_from_frcmod(frcmod_path, mol2_path, d_tup, log=log)
    log.info("[AUTO-BASIS][%s][%s] detected basis from antechamber frcmod: %s", method, d_tup, basis)
    return basis


def run_gaff2_old(
    log,
    cfg,
    ROOT: str,
    method: str,
    d_tup: Tuple[int, int, int, int],
    pdb_for_scan: str,
    basis_periodicities: Optional[Tuple[int, ...]] = None,
    postscan_make_expanded_for_mdgx: bool = True,
    MCS: bool = False,
    mcs_xyz: Optional[str] = None,
    step_size: int = 10,
    net_charge: int = 0,
    spin: int = 1,
) -> None:
    if basis_periodicities is None:
        basis_periodicities = (-6, -4, -3, -2, 1)

    step_size = validate_step_size(step_size)
    log.info("[GAFF2/old][%s][%s] refinement (MCS=%s, step_size=%s)", method, d_tup, MCS, step_size)

    ref = Gaff2SingleDihedralRefinement(cfg=cfg, log=log)
    ref.refine_old(
        root=ROOT,
        method=method,
        d_tup=d_tup,
        basis_periodicities=tuple(int(x) for x in basis_periodicities),
        input_pdb=pdb_for_scan,
        net_charge=net_charge,
        spin=spin,
        MCS=MCS,
        mcs_xyz=mcs_xyz,
        postscan_make_expanded_for_mdgx=postscan_make_expanded_for_mdgx,
        step_size=step_size,
    )


