def norm_flag(x: Optional[str]) -> Optional[str]:
    if x is None:
        return None
    s = str(x).strip().lower()
    return None if s in ("none", "", "null") else s


def validate_step_size(step_size: int) -> int:
    try:
        step = int(step_size)
    except Exception as exc:
        raise ValueError(f"step_size must be int-like, got {step_size!r}") from exc
    if step <= 0:
        raise ValueError(f"step_size must be > 0, got {step}")
    if 360 % step != 0:
        raise ValueError(f"step_size must divide 360 exactly, got {step}")
    return step


def validate_user_dihedral_indices(pdb_file: str, d_tup: Tuple[int, int, int, int]) -> None:
    """Validate that (a, b, c, d) form a bonded dihedral in the PDB (0-based indices)."""
    mol = Chem.MolFromPDBFile(pdb_file, removeHs=False)
    if mol is None:
        raise ValueError(f"RDKit could not read PDB: {pdb_file}")

    n = mol.GetNumAtoms()
    for idx in d_tup:
        if not (0 <= idx < n):
            raise ValueError(f"Atom index {idx} out of range [0, {n - 1}]")

    a, b, c, d = d_tup

    def bonded(i, j):
        return mol.GetBondBetweenAtoms(int(i), int(j)) is not None

    if not (bonded(a, b) and bonded(b, c) and bonded(c, d)):
        raise ValueError(
            f"Indices {list(d_tup)} do not define a bonded dihedral "
            "(required bonds: a-b, b-c, c-d)."
        )


# ============================================================================
