def run_nn_scan(
    log,
    scanner,
    pdb_for_scan: str,
    calc,
    d_tup: Tuple[int, int, int, int],
    label: str,
    method: str,
    step_size: int = 10,
) -> None:
    step_size = validate_step_size(step_size)
    log.info("[NN][%s][%s] scan (label=%s, step_size=%s)", method, d_tup, label, step_size)
    scanner.scan(pdb_for_scan, calc, d_tup, label, step_size=step_size)


