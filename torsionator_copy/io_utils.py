# ============================================================================
# Expanded inputs (one-time postscan setup for mdgx)
# ============================================================================

def _ensure_expanded_inputs(
    cfg,
    log,
    method: str,
    d_tup: Tuple[int, int, int, int],
    basis_periodicities: Tuple[int, ...],
    expanded_tag: str = "expanded",
    tleap_cmd: str = "tleap",
) -> Tuple[str, str]:
    scan_root = _scan_root(cfg, d_tup)
    gaff2_old_method = os.path.join(scan_root, "GAFF2", "old", method)

    ante_exp = os.path.join(gaff2_old_method, f"antechamber_{expanded_tag}")
    leap_exp = os.path.join(gaff2_old_method, f"leap_{expanded_tag}")
    top_exp = os.path.join(leap_exp, "MOL.top")
    frcmod_exp = os.path.join(ante_exp, "MOL.frcmod")

    if os.path.isfile(top_exp) and os.path.isfile(frcmod_exp):
        log.info("[EXPANDED][%s][%s] reusing existing expanded inputs", method, d_tup)
        return top_exp, frcmod_exp

    ref = Gaff2SingleDihedralRefinement(cfg=cfg, log=log, tleap_cmd=tleap_cmd)
    return ref.postscan_expand_old_and_releap(
        method=method,
        d_tup=d_tup,
        basis_periodicities=tuple(int(x) for x in basis_periodicities),
        expanded_tag=expanded_tag,
    )


# ============================================================================
# cpptraj: XYZ → NC
# ============================================================================

def _ensure_nc_from_xyz(
    cfg,
    log,
    method: str,
    d_tup: Tuple[int, int, int, int],
    MCS: bool,
    use_old_expanded: bool,
) -> str:
    method_dir = _method_dir(cfg, d_tup, method, MCS)
    method_subdir = _method_subdir(cfg, d_tup, method, MCS)
    scan_dir = _scan_root(cfg, d_tup)

    leap_subdir = (
        f"GAFF2/old/{method}/leap_expanded"
        if use_old_expanded
        else f"GAFF2/old/{method}/leap"
    )

    xyz_path = os.path.join(method_dir, "geometries.xyz")
    nc_path = os.path.join(method_dir, "geometries.nc")

    if not os.path.isfile(xyz_path):
        raise FileNotFoundError(f"Missing geometries.xyz at {xyz_path}")

    write_xyz_to_nc_in(scan_dir, leap_subdir, method_subdir)

    inp_path = os.path.join(method_dir, "xyz_to_nc.in")
    if not os.path.isfile(inp_path):
        raise FileNotFoundError(f"cpptraj input not created: {inp_path}")

    log.info("[CPPTRAJ][%s][%s] xyz -> nc", method, d_tup)
    subprocess.run(
        ["cpptraj", "-i", "xyz_to_nc.in"],
        cwd=method_dir,
        check=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
    )

    if not os.path.isfile(nc_path):
        raise FileNotFoundError(f"cpptraj did not produce geometries.nc at {nc_path}")

    return nc_path


