def write_xyz_file(
    filename: str,
    atoms,
    mode: str = "a",
    sig: int = 16,
    header: str = "Generated by torsionator",
) -> None:
    os.makedirs(os.path.dirname(filename) or ".", exist_ok=True)
    fmt = f".{sig}g"
    lines = [f"{len(atoms)}\n", f"{header}\n"]
    for atom in atoms:
        x, y, z = atom.position
        lines.append(f"{atom.symbol} {format(x, fmt)} {format(y, fmt)} {format(z, fmt)}\n")
    with open(filename, mode, encoding="utf-8") as f:
        f.writelines(lines)


def write_pdb(path: str, atoms) -> None:
    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
    write_proteindatabank(path, atoms)


