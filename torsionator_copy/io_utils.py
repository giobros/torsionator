def parameterize_one_dihedral(
    cfg,
    log,
    m: str,
    d_tup: Tuple[int, int, int, int],
    MCS: bool = False,
    hrst_values: Optional[List[float]] = None,
    basis_periodicities: Optional[Tuple[int, ...]] = None,
    expand_basis: bool = True,
) -> Dict[float, Dict[str, str]]:
    if basis_periodicities is None:
        basis_periodicities = (-6, -4, -3, -2, 1)
    if hrst_values is None:
        hrst_values = sorted({float(f"{x:.8g}") for x in np.logspace(-6, -1, num=30)})
    else:
        hrst_values = sorted({float(f"{x:.8g}") for x in hrst_values})

    scan_dir = _scan_root(cfg, d_tup)
    os.makedirs(scan_dir, exist_ok=True)
    method_dir = _method_dir(cfg, d_tup, m, MCS)
    os.makedirs(method_dir, exist_ok=True)
    method_subdir = _method_subdir(cfg, d_tup, m, MCS)

    ante_dir = _gaff2_old_ante_dir(cfg, d_tup, m)
    mol2_path = os.path.join(ante_dir, "MOL.mol2")
    frcmod_base_path = os.path.join(ante_dir, "MOL.frcmod")

    if not os.path.isfile(mol2_path):
        raise FileNotFoundError(f"Missing MOL.mol2 for {d_tup} at {mol2_path}")
    if not os.path.isfile(frcmod_base_path):
        raise FileNotFoundError(f"Missing base MOL.frcmod at {frcmod_base_path}")

    use_old_expanded = False
    frcmod_for_mdgx = frcmod_base_path

    if expand_basis:
        _top_exp, frcmod_exp = _ensure_expanded_inputs(
            cfg=cfg,
            log=log,
            method=m,
            d_tup=d_tup,
            basis_periodicities=tuple(int(x) for x in basis_periodicities),
        )
        use_old_expanded = True
        frcmod_for_mdgx = frcmod_exp
    else:
        log.info("[PARAM][%s][%s] expand_basis=False -> using original GAFF2/old frcmod", m, d_tup)

    _ensure_nc_from_xyz(cfg=cfg, log=log, method=m, d_tup=d_tup, MCS=MCS, use_old_expanded=use_old_expanded)

    atom_types = get_atom_types_from_mol2(mol2_path)
    results: Dict[float, Dict[str, str]] = {}

    for hrst_val in hrst_values:
        pfile, dat_path, out_path = param_input_file(
            atom_types=atom_types,
            method=m,
            dihedral_indices=d_tup,
            scan_dir=scan_dir,
            method_subdir=method_subdir,
            hrst_val=hrst_val,
            use_old_expanded=use_old_expanded,
        )

        subprocess.run(
            ["mdgx", "-i", os.path.basename(pfile)],
            cwd=os.path.dirname(pfile),
            check=True,
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
        )

        frcmod_dir = os.path.join(os.path.dirname(dat_path), "frcmod")
        os.makedirs(frcmod_dir, exist_ok=True)
        frcmod_path = os.path.join(frcmod_dir, f"{hrst_val}.frcmod")
        # When --multiplicity is used (expand_basis=True) the mdgx run already
        # uses the expanded frcmod with the requested basis periodicities.  For
        # the per-hrst output frcmod we start from the original GAFF2/old frcmod
        # (same multiplicities as parmchk2) and then *replace* the target-dihedral
        # block entirely with the newly fitted terms from the .dat, so no old
        # GAFF2 barrier values leak into GAFF2/new/hrst/antechamber/MOL.frcmod.
        frcmod_base_for_output = frcmod_base_path if expand_basis else frcmod_for_mdgx
        shutil.copy2(frcmod_base_for_output, frcmod_path)

        frcmod_path = update_frcmod(
            frcmod_path=frcmod_path,
            dat_path=dat_path,
            method=m,
            output_folder=frcmod_dir,
            replace_dat_keys=not expand_basis,
        )

        results[hrst_val] = {
            "input": pfile,
            "dat": dat_path,
            "out": out_path,
            "frcmod": frcmod_path,
        }

    if not results:
        raise RuntimeError(f"[PARAM][{m}][{d_tup}] all hrst runs failed")

    return results


def run_parametrization(
    log,
    cfg,
    method: str,
    d_tup: Tuple[int, int, int, int],
    MCS: bool = False,
    basis_periodicities: Optional[Tuple[int, ...]] = None,
    expand_basis: bool = True,
) -> Dict[float, Dict[str, str]]:
    log.info("[PARAM] start %s %s (MCS=%s expand_basis=%s)", method, d_tup, MCS, expand_basis)
    return parameterize_one_dihedral(
        cfg=cfg,
        log=log,
        m=method,
        d_tup=d_tup,
        MCS=MCS,
        basis_periodicities=basis_periodicities,
        expand_basis=expand_basis,
    )


# ============================================================================
# Cumulative frcmod bookkeeping
# ============================================================================

def update_cumulative_frcmod(
    cfg,
    final_frcmod: Dict[str, str],
    log,
    ROOT: str,
    m: str,
    d_tup: Tuple[int, int, int, int],
    dat_path: str,
    frcmod_base_path: str,
    tag: str = "",
    expand_basis: bool = True,
) -> None:
    dstr = _dstr(d_tup)
    key = m + (tag or "")

    final_dir = os.path.join(cfg.base_dir, "parameters")
    os.makedirs(final_dir, exist_ok=True)

    dihedral_frcmod = os.path.join(final_dir, f"{ROOT}_{m}{tag}_{dstr}.frcmod")
    shutil.copy2(frcmod_base_path, dihedral_frcmod)
    update_frcmod(dihedral_frcmod, dat_path, m, final_dir, replace_dat_keys=not expand_basis)

    cumulative_frcmod = final_frcmod.get(key)
    if cumulative_frcmod is None:
        cumulative_frcmod = os.path.join(final_dir, f"{ROOT}_{m}{tag}.frcmod")
        shutil.copy2(frcmod_base_path, cumulative_frcmod)
        final_frcmod[key] = cumulative_frcmod

    log.info("[FRCMOD%s][%s][%s] merge dat into cumulative: %s", tag, m, d_tup, cumulative_frcmod)
    update_frcmod(cumulative_frcmod, dat_path, m, final_dir, replace_dat_keys=not expand_basis)

    # Absorb any new_<m>.frcmod or new.frcmod produced by update_frcmod
    for candidate in (os.path.join(final_dir, f"new_{m}.frcmod"), os.path.join(final_dir, "new.frcmod")):
        if os.path.exists(candidate):
            os.replace(candidate, cumulative_frcmod)
            log.info("[FRCMOD%s][%s] updated cumulative using %s", tag, m, os.path.basename(candidate))
            break


def select_and_update_frcmod(
    log,
    cfg,
    ROOT: str,
    method: str,
    d_tup: Tuple[int, int, int, int],
    final_frcmod: Dict[str, str],
    param_results: Dict[float, Dict[str, str]],
    tag: str = "",
    MCS: bool = False,
    expand_basis: bool = True,
) -> str:
    nn_energy_file = _nn_energy_file(cfg, d_tup, method, MCS=MCS)
    if not os.path.exists(nn_energy_file):
        raise FileNotFoundError(
            f"[SELECT{tag}][{method}][{d_tup}] NN reference not found: {nn_energy_file}"
        )

    gaff2_new_dir = _gaff2_new_dir(cfg, d_tup, method)
    best_hrst, _best_frcmod_src, _best_rmse, best_profile_file = find_best_hrst_by_RMSE(
        nn_energy_file=nn_energy_file,
        gaff2_new_dir=gaff2_new_dir,
        log=log,
    )

    if best_hrst not in param_results:
        raise KeyError(
            f"Best hrst={best_hrst} not in param_results keys: {sorted(param_results.keys())}"
        )

    best_dat_path = param_results[best_hrst]["dat"]
    frcmod_base_path = os.path.join(_gaff2_old_ante_dir(cfg, d_tup, method), "MOL.frcmod")
    if not os.path.exists(frcmod_base_path):
        raise FileNotFoundError(f"Base MOL.frcmod not found at {frcmod_base_path}")

    update_cumulative_frcmod(
        cfg=cfg,
        final_frcmod=final_frcmod,
        log=log,
        ROOT=ROOT,
        m=method,
        d_tup=d_tup,
        dat_path=best_dat_path,
        frcmod_base_path=frcmod_base_path,
        tag=tag,
        expand_basis=expand_basis,
    )

    return best_profile_file
