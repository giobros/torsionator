def minimize_input(cfg, opt, calc_factory, log, pdb_file: str, methods: List[str]) -> Dict[str, str]:
    out: Dict[str, str] = {}
    for m in methods:
        log.info("→ Minimizing input with %s", m)
        calc, label = calc_factory.get(m)
        out_path = os.path.join(cfg.base_dir, f"minimized_{label}.pdb")
        opt.minimize_and_write_pdb(pdb_file, calc, out_path)
        out[m] = out_path
    return out


