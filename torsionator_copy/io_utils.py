def _dstr(d_tup: Tuple[int, int, int, int]) -> str:
    return "_".join(map(str, d_tup))


def _scan_root(cfg, d_tup: Tuple[int, int, int, int]) -> str:
    return os.path.join(cfg.base_dir, "scanning", _dstr(d_tup))


def _method_dir(cfg, d_tup: Tuple[int, int, int, int], method: str, MCS: bool) -> str:
    root = _scan_root(cfg, d_tup)
    sub = os.path.join(method, "MCS") if MCS else method
    return os.path.join(root, sub)


def _method_subdir(cfg, d_tup: Tuple[int, int, int, int], method: str, MCS: bool) -> str:
    return os.path.relpath(_method_dir(cfg, d_tup, method, MCS), _scan_root(cfg, d_tup))


def _nn_energy_file(cfg, d_tup: Tuple[int, int, int, int], method: str, MCS: bool) -> str:
    return os.path.join(_method_dir(cfg, d_tup, method, MCS), "angles_vs_energies_final.txt")


def _gaff2_old_dir(cfg, d_tup: Tuple[int, int, int, int], method: str) -> str:
    return os.path.join(_scan_root(cfg, d_tup), "GAFF2", "old", method)


def _gaff2_new_dir(cfg, d_tup: Tuple[int, int, int, int], method: str) -> str:
    return os.path.join(_scan_root(cfg, d_tup), "GAFF2", "new", method)


def _gaff2_old_ante_dir(cfg, d_tup: Tuple[int, int, int, int], method: str) -> str:
    return os.path.join(_gaff2_old_dir(cfg, d_tup, method), "antechamber")


